    public static function fromMSISDNValue(string $value)
    {
        $possibleCountryCodeValues = [];
        foreach(self::toArray() as $countryCodeValue) {
            if(strpos($value, $countryCodeValue) !== 0) {
                continue;
            }

            $possibleCountryCodeValues[] = $countryCodeValue;
        }

        // Sort possible countries from the longest string length to shortest
        usort($possibleCountryCodeValues, function($a, $b) {
            return strlen($b) - strlen($a);
        });

        $longestValue = array_shift($possibleCountryCodeValues);

        return new self($longestValue);
    }

    public function value(): string
    {
        return $this->value;
    }
